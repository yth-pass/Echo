/**
 * 检测回复是否包含 AI 腔黑名单表达。
 * @returns 命中的黑名单表达列表（空 = 通过）
 */
export function detectAiSpeech(text: string): string[] {
  const hits: string[] = [];
  for (const phrase of AI_SPEECH_BLACKLIST) {
    if (text.includes(phrase)) {
      hits.push(phrase);
    }
  }
  return hits;
}

