/**
 * Scan `text` against all sensitive-word categories.
 *
 * Returns `{ hit: false, words: [] }` on pass, or `{ hit: true, words, category }`
 * with the first matched category on hit.
 */
export function checkSensitiveWords(text: string): SensitiveCheckResult {
  if (!text) return { hit: false, words: [] };

  for (const cat of CATEGORIES) {
    const matches: string[] = [];
    for (const re of cat.patterns) {
      // Reset lastIndex for global regexes
      re.lastIndex = 0;
      let m: RegExpExecArray | null;
      while ((m = re.exec(text)) !== null) {
        matches.push(m[0]);
        // Guard against infinite loops on zero-length matches
        if (m[0].length === 0) {
          re.lastIndex++;
        }
      }
    }
    if (matches.length > 0) {
      return {
        hit: true,
        words: [...new Set(matches)],
        category: cat.name,
      };
    }
  }

  return { hit: false, words: [] };
}
