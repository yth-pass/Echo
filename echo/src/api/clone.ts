export type IdealPartnerCardInput = {
  cardId: string;
  choice: string;
  freeText?: string;
};

/**
 * POST /clones/me/ideal-partner/cards — 补答 3 道理想伴侣探测卡。
 * 合并答案到 Profile.bioJson + 重算 idealPartnerDimensions，不生成 sketch。
 * 返回 ApiResult 以便前端展示错误（如非法 cardId）。
 */
export async function submitIdealPartnerCards(
  cards: IdealPartnerCardInput[],
): Promise<ApiResult<CloneMe>> {
  if (!getApiBaseUrl()) return { ok: false, status: -1, message: 'no-base' };
  const result = await apiPostJson<
    { cards: IdealPartnerCardInput[] },
    Record<string, unknown>
  >('/clones/me/ideal-partner/cards', { cards });
  if (result.ok) {
    return { ok: true, data: mapCloneMe(result.data) };
  }
  return result as unknown as ApiResult<CloneMe>;
}

/**
 * POST /clones/me/ideal-partner/generate — 生成理想型描述 + 重算 ideal_embedding。
 * 前置：3 道探测卡已答（后端校验，未答返回 400 提示先补答）。
 * 返回 ApiResult 以便前端区分"未答卡"(400) / "LLM 不可用"(503) / 网络错误。
 */
export async function generateIdealPartnerClone(): Promise<ApiResult<CloneMe>> {
  if (!getApiBaseUrl()) return { ok: false, status: -1, message: 'no-base' };
  const result = await apiPostJson<Record<string, never>, Record<string, unknown>>(
    '/clones/me/ideal-partner/generate',
    {} as Record<string, never>,
  );
  if (result.ok) {
    return { ok: true, data: mapCloneMe(result.data) };
  }
  return result as unknown as ApiResult<CloneMe>;
}
